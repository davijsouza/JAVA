package Strings;

public class Strings_01 {
    public static void main(String[] args) {

        /** String são imutáveil.
         * Alocando valores diretamente no pool
         */
        String nome = "Nome";


        /**
         * Alocando valores no objeto.
         * Alocando valores de referencia.
         * Alocando valores no pool de strings
         * Aqui podemos usar metodos de buscar e comparações.
         */
        String nome2 = new String("Nome2");

    }
}
